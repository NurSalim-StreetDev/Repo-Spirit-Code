import numpy as np

# Membuat array dari list biasa
array2D = [[1, 2, 3, 4], [1, 2, 3, 4]]
print(f'array2D biasa: {array2D}')

# Menampilkan array buatan sendiri
print(f'contoh np.ones: {array2D}')

# Membuat array menggunakan numpy
Numpy_1D_ones = np.ones(1)
Numpy_1D_zeros = np.zeros(1)
Numpy_1D_arange = np.arange(1)

# Menampilkan hasil
print(f'Numpy_1D Method ones: {Numpy_1D_ones}')
print(f'Numpy_1D Method zeros: {Numpy_1D_zeros}')
print(f'Numpy_1D Method arange: {Numpy_1D_arange}')
