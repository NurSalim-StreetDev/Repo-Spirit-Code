'''Mari kita coba latihan Aritmatika'''
# Aritmatika Pertambahan
x = 5
y = 6
z = 8
hasil = x + y + z
print (f'Rumus: ', x, '+', y, '+', z, '= ', hasil, 'Type hasil', type(hasil))

# Aritmatika Pengurangan
x = 5
y = 6
z = 8
hasil = x - y - z
print (f'Rumus: ', x, '-', y, '-', z, '= ', hasil, 'Type hasil', type(hasil))

# Aritmatika perkalian
x = 5
y = 6
z = 8
hasil = x * y * z
print (f'Rumus: ', x, '*', y, '*', z, '= ', hasil, 'Type hasil', type(hasil))

# Aritmatika Pembagian
x = 5
y = 6
z = 8
hasil = x / y / z
print (f'Rumus: ', x, '/', y, '/', z, '= ', hasil, 'Type hasil', type(hasil))

# Aritmatika Modulus
x = 5
y = 6
z = 8
hasil = x % y % z
print (f'Rumus: ', x, '%', y, '%', z, '= ', hasil, 'Type hasil', type(hasil))

# Aritmatika floor division
x = 5
y = 6
z = 8
hasil = x // y // z
print (f'Rumus: ', x, '//', y, '//', z, '= ', hasil, 'Type hasil', type(hasil))

# Aritmatika Ekspinensial
x = 5
y = 6
z = 8
hasil = (x ** y) ** z
print (f'Rumus: ', (x, '**', y), '**', z, '= ', hasil, 'Type hasil', type(hasil))

# Aritmatika Campuranya
x = 5
y = 6
z = 8
hasil = x * y ** z + x - y / z % x // y
print (f'Rumus: ', x, '*', y, '**', z, '+', x, '-', y, '/', z, '%', x, '//', y, '=', hasil, 'Type hasil', type(hasil))

